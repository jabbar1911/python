# Say "Hello, World!" With Python
print("Hello, World!")  # Output: Hello, World!
print("\n" + "-"*30 + "\n")



# Python If-Else: Check if a number is odd or even
num = int(input("Enter a number: "))  # Example input: 4
if num % 2 == 0:
    print("Even")  # Output if input 4: Even
else:
    print("Odd")   # Output if input 5: Odd
print("\n" + "-"*30 + "\n")



# Arithmetic Operators: Sum and Multiplication
a = int(input("Enter first number: "))  # Example: 3
b = int(input("Enter second number: ")) # Example: 5
print("Sum:", a + b)       # Output: Sum: 8
print("Product:", a * b)   # Output: Product: 15
print("\n" + "-"*30 + "\n")



# Loops: Print first n numbers
n = int(input("Enter n: "))  # Example: 5
for i in range(1, n + 1):
    print(i, end=" ")        # Output for n=5: 1 2 3 4 5
print("\n" + "-"*30 + "\n")
